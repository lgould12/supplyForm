<template>
    <div style="margin: 25px;">
            <div>
                <ul class="nav nav-tabs">
                    <li v-for="(tab,index) in tabs" :key="index" class="nav-item">
                        <a v-if="tab.isActive" class="nav-link active" :href="tab.href" @click="selectTab(tab)">{{ tab.name }}</a>
                        <a v-else class="nav-link" :href="tab.href" @click="selectTab(tab)">{{ tab.name }}</a>
                    </li>
                </ul>
            </div>
            <div class="tabs-details">
                <slot></slot>
            </div>
    </div>
</template>

<script>
export default {
  name: 'Tabs',
  data () {
    return { tabs: [] }
  },

  created () {
    this.tabs = this.$children
  },

  methods: {
    selectTab (selectedTab) {
      this.tabs.forEach(tab => {
        tab.isActive = (tab.href == selectedTab.href)
      })
    }
  }
}
</script>
